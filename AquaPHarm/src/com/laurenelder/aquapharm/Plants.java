package com.laurenelder.aquapharm;

public class Plants {

	public String name;
	public Integer minTemp;
	public Integer maxTemp;
	public String image;
	
	public Plants(String plantName, Integer minPlantTemp, Integer maxPlantTemp, String plantImage) {
		this.name = plantName;
		this.minTemp = minPlantTemp;
		this.maxTemp = maxPlantTemp;
		this.image = plantImage;
	}
	
	public String toString() {
		return name;
	}
	
}
